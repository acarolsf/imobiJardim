	private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btExcluirActionPerformed
		// TODO add your handling code here:
		int a = Integer.parseInt(txtId_venda.getText()); // pega oq foi digitado
		if ("".equals(a)) {
			dispose();
			JOptionPane.showMessageDialog(null, "Digite o codigo da venda!");
			TelaExcluirVenda tbc = new TelaExcluirVenda();
			tbc.setVisible(true);

		} else {
			dao.excluir(a); // chama o metodo para excluir convertendo-o para inteiro
			txtId_venda.setText(""); // limpar
			JOptionPane.showMessageDialog(null, "Venda excluida!\n");
			dispose();// fecha a tela
			MenuTabelas mt = new MenuTabelas();
			mt.setVisible(true);
		}
	}// GEN-LAST:event_btExcluirActionPerformed

